import json
import requests

def main():

    #--------------------------------------------------------------------------------------------------------------------------
    #    Change server
    #--------------------------------------------------------------------------------------------------------------------------
    
    server = "http://localhost:8080"
    
    #--------------------------------------------------------------------------------------------------------------------------
    #    Change data file
    #--------------------------------------------------------------------------------------------------------------------------
    
    museum = 'GAM'
    filename = 'gam1.json'

    #--------------------------------------------------------------------------------------------------------------------------
    #    Insert perspective
    #--------------------------------------------------------------------------------------------------------------------------

    route = "perspectives/" + museum + "/" + filename
    file = open(route)
    perspective = json.load(file)
    
    response = requests.post("{}/v1.1/perspective".format(server), json=perspective)
    print(response)
    print(response.text)
    

main()
